/**
 * Cube + CHGCAR integration tests.
 *
 * These exercise the **TS integration boundary**: format inference,
 * reader dispatch in `openTextReader`, the `DrawIsosurfaceModifier`'s
 * auto-attach predicate, and the marching-cubes path the modifier
 * drives. Parser correctness across the full fixture set is covered by
 * the Rust-side `tests/test_io/test_{cube,chgcar}.rs` tests; loading
 * those large fixtures from a browser test environment is impractical
 * (no FS access), so we use a minimal inlined cube fixture plus
 * synthetic in-memory frames.
 */

